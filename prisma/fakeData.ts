const parseMoneyToInt = (value: number | string): number => {
  if (typeof value === "number") {
    return Math.round(value);
  }
  const cleaned = value.replace(/[^\d]/g, "");
  const parsed = parseInt(cleaned, 10);
  return Number.isNaN(parsed) ? 0 : parsed;
};

